    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (TabelaView.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione o item que deseja modificar!");
            return;
        }

        ContatoGrid g = (ContatoGrid) TabelaView.getModel();

        new FormContato(this, g.getItem(TabelaView.getSelectedRow()), dados).setVisible(true);

        setAtualizarDados();

    }//GEN-LAST:event_jButton2ActionPerformed

