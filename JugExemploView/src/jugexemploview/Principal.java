    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (TabelaView.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione o item que deseja deletar!");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Confirma exclus√£o?") != JOptionPane.YES_OPTION) {
            return;
        }

        ContatoGrid g = (ContatoGrid) TabelaView.getModel();

        try {
            dados.Deletar(g.getItem(TabelaView.getSelectedRow()));
        } catch (Exception ex) {
            System.out.println("Erro ao deletar contato!");
            System.err.println(ex.getMessage());
        }
        setAtualizarDados();
    }//GEN-LAST:event_jButton3ActionPerformed

