    private void btnSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvaActionPerformed
        // TODO add your handling code here:

        if (edContato.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Informe o nome do contato!");
            return;
        }

        if (edTelefone.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Informe o n√∫m. do telefone do contato!");
            return;
        }

        this.cadastro.setNome(edContato.getText());
        this.cadastro.setTelefone(edTelefone.getText());
        this.cadastro.setTipo((TContato) cbTipo.getSelectedItem());

        try {

            if (this.cadastro.getId() == 0) {
                dados.Inserir(cadastro);
            } else {
                dados.Alterar(cadastro);
            }
            
            dispose();
        } catch (Exception e) {
            System.out.println("Erro no manipular dados.");
            System.err.println(e.getMessage());
        }

    }//GEN-LAST:event_btnSalvaActionPerformed


