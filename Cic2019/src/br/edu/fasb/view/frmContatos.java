    private class GridDados extends AbstractTableModel {

        String colunas[] = {"Id", "Nome", "Telefone", "E-mail"};
        List<Contato> lista;

        public GridDados(List<Contato> lista) {
            this.lista = lista;
        }

        @Override
        public int getRowCount() {
            return lista.size();
        }

        @Override
        public String getColumnName(int column) {
            return colunas[column];
        }

        @Override
        public int getColumnCount() {
            return colunas.length;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            switch (columnIndex) {
                case 0:
                    return lista.get(rowIndex).getId();
                case 1:
                    return lista.get(rowIndex).getNome();
                case 2:
                    return lista.get(rowIndex).getTelefone();
                case 3:
                    return lista.get(rowIndex).getEmail();
                default:
                    return "";
            }
        }

        public Contato getContato(int rowIndex) {
            return lista.get(rowIndex);
        }
    }

