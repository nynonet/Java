    private void AtualizarGrid() {
        try {
            gridContatos.setModel(new GridDados(dados.getContatos("")));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void Controlebotoes(boolean ativar) {

        btnNovo.setEnabled(ativar);
        btnAlterar.setEnabled(ativar);
        btnSalvar.setEnabled(!ativar);
        btnCancelar.setEnabled(!ativar);
        btnDeletar.setEnabled(ativar);
        btnFechar.setEnabled(ativar);

        edNome.setEditable(!ativar);
        edTelefone.setEditable(!ativar);
        edEmail.setEditable(!ativar);

        if (gridContatos.getRowCount() == 0) {
            btnDeletar.setEnabled(false);
            btnAlterar.setEnabled(false);
        }

        gridContatos.setEnabled(ativar);
    }

    private void LimpaTela() {
        edNome.setText("");
        edTelefone.setText("");
        edEmail.setText("");
    }

    private void CarregaTela() {

        if (gridContatos.getRowCount() == 0) {
            LimpaTela();
            return;
        }

        GridDados gd = (GridDados) gridContatos.getModel();
        Contato c = gd.getContato(gridContatos.getSelectedRow());
        edNome.setText(c.getNome());
        edTelefone.setText(c.getTelefone());
        edEmail.setText(c.getEmail());
    }

