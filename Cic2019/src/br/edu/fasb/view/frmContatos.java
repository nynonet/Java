    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (edNome.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Informe o nome do contato!");
            edNome.requestFocus();
            return;
        }

        if (edTelefone.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Informe o nome do telefone!");
            edTelefone.requestFocus();
            return;
        }

        Contato c;

        if (acaoUpdate) {
            //carrega o contato selecionado 
            GridDados gd = (GridDados) gridContatos.getModel();
            c = gd.getContato(gridContatos.getSelectedRow());
        } else {
            //gera um novo contato
            c = new Contato();
        }

        c.setNome(edNome.getText());
        c.setTelefone(edTelefone.getText());
        c.setEmail(edEmail.getText());

        try {
            dados.gravar(c);
            JOptionPane.showMessageDialog(this, dados.getMensagem());
        } catch (SQLException e) {
            e.printStackTrace();
        }
        AtualizarGrid();
        Controlebotoes(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

