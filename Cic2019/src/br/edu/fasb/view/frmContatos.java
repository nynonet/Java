    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        // TODO add your handling code here:

        if (gridContatos.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "É necessário selecionar o registro que deseja deletar!");
            return;
        }

        GridDados gd = (GridDados) gridContatos.getModel();
        Contato c = gd.getContato(gridContatos.getSelectedRow());

        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente apagar o contato?", "Deletar Contato", JOptionPane.YES_NO_OPTION);

        if (opcao == JOptionPane.YES_OPTION) {
            try {
                System.out.println(c);
                dados.deletar(c);
                JOptionPane.showMessageDialog(this, dados.getMensagem());
            } catch (SQLException e) {
                e.printStackTrace();
            }

            AtualizarGrid();
        }

    }//GEN-LAST:event_btnDeletarActionPerformed

